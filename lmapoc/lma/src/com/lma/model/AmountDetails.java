package com.lma.model;

/**
 * @author RAHUL
 *
 */
public class AmountDetails {

	
	private String subtotal;
	private String tax;
	private String  shipping;
	private String handling_fee;
	private String shipping_discount;
	private String insurance;
	/**
	 * @return the subtotal
	 */
	public String getSubtotal() {
		return subtotal;
	}
	/**
	 * @param subtotal the subtotal to set
	 */
	public void setSubtotal(String subtotal) {
		this.subtotal = subtotal;
	}
	/**
	 * @return the tax
	 */
	public String getTax() {
		return tax;
	}
	/**
	 * @param tax the tax to set
	 */
	public void setTax(String tax) {
		this.tax = tax;
	}
	/**
	 * @return the shipping
	 */
	public String getShipping() {
		return shipping;
	}
	/**
	 * @param shipping the shipping to set
	 */
	public void setShipping(String shipping) {
		this.shipping = shipping;
	}
	/**
	 * @return the handling_fee
	 */
	public String getHandling_fee() {
		return handling_fee;
	}
	/**
	 * @param handling_fee the handling_fee to set
	 */
	public void setHandling_fee(String handling_fee) {
		this.handling_fee = handling_fee;
	}
	/**
	 * @return the shipping_discount
	 */
	public String getShipping_discount() {
		return shipping_discount;
	}
	/**
	 * @param shipping_discount the shipping_discount to set
	 */
	public void setShipping_discount(String shipping_discount) {
		this.shipping_discount = shipping_discount;
	}
	/**
	 * @return the insurance
	 */
	public String getInsurance() {
		return insurance;
	}
	/**
	 * @param insurance the insurance to set
	 */
	public void setInsurance(String insurance) {
		this.insurance = insurance;
	}
	
	
	
	
}
